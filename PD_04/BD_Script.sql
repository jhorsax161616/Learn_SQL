SET DATEFORMAT DMY

CREATE TABLE tblPasajero (
DNI VARCHAR(8) PRIMARY KEY,
pasApellidoPaterno VARCHAR(35) NOT NULL,
pasApellidoMaterno VARCHAR(35),
pasNombres VARCHAR(50) NOT NULL,
pasTelefono VARCHAR(12),
pasDireccion VARCHAR(120),
pasPuntos INT)

CREATE TABLE tblTripulacion (
IDTripulacion VARCHAR(7) PRIMARY KEY,
triApellidoPaterno VARCHAR(35) NOT NULL,
triApellidoMaterno VARCHAR(35),
triNombres VARCHAR(50),
triTelefono VARCHAR(12),
triEmail VARCHAR(80))

CREATE TABLE tblRuta (
IDRuta VARCHAR(7) PRIMARY KEY,
rutOrigen VARCHAR(50) NOT NULL,
rutDestino VARCHAR(50) NOT NULL,
rutPrecio DECIMAL(7,2))

CREATE TABLE tblViaje (
IDViaje VARCHAR(15) PRIMARY KEY,
viaFecha DATE NOT NULL,
viaHora TIME NOT NULL,
IDRuta VARCHAR(7) NOT NULL,
FOREIGN KEY (IDRuta) REFERENCES tblRuta(IDRuta))

CREATE TABLE tblPasaje (
IDPasaje VARCHAR(13) PRIMARY KEY,
DNI VARCHAR(8) NOT NULL,
IDViaje VARCHAR(15) NOT NULL,
FOREIGN KEY (DNI) REFERENCES tblPasajero(DNI),
FOREIGN KEY (IDViaje) REFERENCES tblViaje(IDViaje))

CREATE TABLE tblDesignacion (
IDTripulacion VARCHAR(7) NOT NULL,
IDViaje VARCHAR(15) NOT NULL,
PRIMARY KEY (IDViaje,IDTripulacion),
FOREIGN KEY (IDViaje) REFERENCES tblViaje(IDViaje),
FOREIGN KEY (IDTripulacion) REFERENCES tblTripulacion(IDTripulacion))

